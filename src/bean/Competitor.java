package bean;

/**
 *
 * @author User
 */
public class Competitor {
    public String name;
    public String surname;
    public int age;
    public int score;

    public Competitor() {
    }

    public Competitor(String name, String surname, int age, int score) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int win) {
        this.score = score;
    }
    
    
}
